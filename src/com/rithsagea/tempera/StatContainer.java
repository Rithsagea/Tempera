package com.rithsagea.tempera;

public class StatContainer {
	public int attack;
	public int defense;
	public int health;
	
	/**
	 * 
	 * @param attack		value
	 * @param defense	value
	 * @param health		value
	 */
	public StatContainer(int attack, int defense, int health) {
		this.attack = attack;
		this.defense = defense;
		this.health = health;
	}
}
